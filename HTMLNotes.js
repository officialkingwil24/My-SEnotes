// ### What is HTML?
//is the standard markup language for creating Web pages. HTML describes the structure of a Web page. HTML consists of a series of elements. HTML elements tell the browser how to display the content.

// ### How does it relate to the internet?
//HTML is used to create electronic documents (called pages) that are displayed on the World Wide Web. Each page contains a series of connections to other pages called hyperlinks. Every web page you see on the Internet is written using one version of HTML code or another

// ### How does it relate to the "web development stack"
//is a programming language used to describe the structure of information on a web page. Together, HTML, CSS, and JavaScript make up the essential building blocks of websites, with CSS controlling a page's appearance, and JavaScript programming its functionality

// ### How does it relate to JavaScript?
//html tags create objects; JavaScript lets you manipulate those objects.

// ### What are the parts of HTML?
//HTML have 3 parts 
//a line containing HTML version information,
//a declarative header section (delimited by the HEAD element),
//a body, which contains the document's actual content. The body may be implemented by the BODY element or the FRAMESET element.

// ### What are HTML Tags?
//HTML tags begin with the less-than (<) character and end with greater-than (>). These symbols are also called "angle brackets."

// ### What are some commonly used HTML elements?
//is head tag, html tag, body tag, title tag, heading tag, Paragraph tag, Emphasis tag

// ### What are HTML attributes?
//HTML attributes are special words used inside the opening tag to control the element's behaviour. HTML attributes are a modifier of an HTML element type.